<p>&nbsp;&nbsp;<strong style="line-height: 1.5;">RENAULT KANGOO &ndash; ПРАКТИЧНОСТЬ, ДОВЕДЕННАЯ ДО СОВЕРШЕНСТВА</strong></p>
<p>&nbsp;</p>
<p>С момента своего запуска в 1997 году минивэны Kangoo завоевали популярность во многих странах мира. Совершенствуясь с каждым новым поколением этой модели, Renault Kangoo становится комфортней и удобней.</p>
<p>&nbsp;</p>
<p>Дизайн нового Kangoo стал более мощным и уверенным с подтянутыми &ldquo;мускулистыми&rdquo; линиями вместо прежних скругленных крыльев, а передняя часть выполнена в новом дизайнерском стиле Renault. Эмблема Renault стала более заметной и контрастирует с черной радиаторной решеткой за ней. Новые миндалевидные передние фары, обтекаемая форма зеркал и обновленные задние фонари создают ощущение технологичности и высокого качества. Большой округлый бампер выдается за пределы капота, добавляя уверенности образу автомобиля.</p>
<p>&nbsp;</p>
<p>Улучшения коснулись также интерьера автомобиля. Новые опции, такие как панорамная крыша, люк для длинномерных предметов значительно расширят функциональные возможности Kangoo. Инновацией для грузового Kangoo также является версия с двумя полноценными сиденьями для взрослых пассажиров с возможностью трансформации одного из сидений в небольшой столик.</p>
<p>&nbsp;</p>
<p><strong>НОВЫЕ ЭЛЕМЕНТЫ АКТИВНОЙ БЕЗОПАСНОСТИ</strong></p>
<p>&nbsp;</p>
<p>Элементы активной и пассивной безопасности, доступные в Renault Kangoo, сделают путешествие более комфортным. На Новом Kangoo предлагаются следующее оборудование активной и пассивной безопасности: подушка безопасности водителя и пассажиров, а также боковые подушки безопасности, система стабилизации курсовой устойчивости ESP, АБС, система электронного распределения тормозных усилий, а также система экстренного торможения. Также, одной из важнейших эксплуатационных характеристик автомобиля в отношении безопасности движения является обзорность с места водителя. А обзорность в Kangoo &ndash; это одна из отличительных характеристик данного автомобиля.</p>
<p>&nbsp;</p>
<p><strong>ЛИНЕЙКА НАДЕЖНЫХ ДВИГАТЕЛЕЙ</strong></p>
<p>&nbsp;</p>
