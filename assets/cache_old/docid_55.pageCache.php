<p>Новый бензиновый двигатель 1.6л 102 л.с. характеризуется крутящим моментом в 145 Н∙м и расходом топлива от 6.1 л на 100 км.<br />Дизельный двигатель 1.5л 86 л.с. с крутящим моментом 200 Н∙м и расходом топлива от 4,9 л на 100 км.</p>
